#include <stdio.h>

int main() {
    // Sequência de passos
    printf("Passo 1: Início do programa\n");
    printf("Passo 2: Declarando variáveis\n");

    int a = 5;
    int b = 3;
    int soma = a + b;

    printf("Passo 3: Realizando a soma\n");
    printf("Resultado: %d + %d = %d\n", a, b, soma);

    printf("Passo 4: Fim do programa\n");

    return 0;
}
